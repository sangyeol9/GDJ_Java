package com.sangs.s04.util.ex1;

public class Student {

	private String name;
	private int num;
	private int guk;
	private int math;
	private int eng;
	private Double ab;
	private int sum;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getGuk() {
		return guk;
	}
	public void setGuk(int guk) {
		this.guk = guk;
	}
	public int getMath() {
		return math;
	}
	public void setMath(int math) {
		this.math = math;
	}
	public int getEng() {
		return eng;
	}
	public void setEng(int eng) {
		this.eng = eng;
	}
	public Double getAb() {
		return ab;
	}
	public void setAb(Double ab) {
		this.ab = ab;
	}
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
	
}
